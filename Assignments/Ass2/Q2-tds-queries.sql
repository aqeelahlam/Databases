SELECT
    VEH_MAINCOLOR AS "Main Colour",
    VEH_VIN AS VIN,
    TO_CHAR(VEH_YRMANUF, 'YYYY') AS "Year Manufactured"
FROM 
    VEHICLE
WHERE
    (TO_CHAR(VEH_YRMANUF, 'YYYY') >= '2012' AND TO_CHAR(VEH_YRMANUF, 'YYYY') <= '2014') AND
    (VEH_MODNAME = 'Range Rover' OR
    VEH_MODNAME = 'Range Rover Sport')
ORDER BY
    VEH_YRMANUF DESC, 
    VEH_MAINCOLOR ASC;

