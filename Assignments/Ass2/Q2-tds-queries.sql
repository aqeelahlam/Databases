SELECT 
    DEM_POINTS AS "Demerit Points",
    DEM_DESCRIPTION AS "Demerit Descriptions"
FROM
    DEMERIT
WHERE
    DEM_DESCRIPTION like '%Heavy%' or 
    DEM_DESCRIPTION like '%heavy%' or
    DEM_DESCRIPTION like 'Exceed%'
ORDER BY 
    DEM_POINTS, DEM_DESCRIPTION;


